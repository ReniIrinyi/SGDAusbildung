public class EinsendeAufgabe07 {
        public static void main(String[] args) {
            int counter=0;
           while(counter<=100) {
                for(int i =1; i<=100; i++){
                    counter++;
                    System.out.println(i*2);
                }
                break;
           }

        }
    }

